#Write a function to return square of a number.
def square_of_num(n):
    return n**2 #return the square 
#take input from the user
num=int(input("Enter a number: "))
#call the function and print the result
result=square_of_num(num) #call the function with the input number and store the result
print("Square of the number: ", result) #print the square of the number
